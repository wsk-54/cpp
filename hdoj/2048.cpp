#include<iostream>
#include<iomanip>

using namespace std;

int main(){
    int c,n,i;
    int64_t a[21]={0,1,2,6},b[21]={0,0,1,2};
    for(i=3;i<=21;i++){
        a[i]=a[i-1]*i;
        b[i]=(i-1)*(b[i-1]+b[i-2]);
    }
    cin>>c;
    while(c--){
        cin>>n;
        cout<<fixed<<setprecision(2)<<100.0*b[n]/a[n]<<"%"<<endl;
    }
    return 0;
}

